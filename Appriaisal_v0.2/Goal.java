public class Goal {

    private String goalName;
    private Integer goalWeightageValue = 0;
    private String goalAchieved;

    public String getGoalName() {
        return goalName;
    }

    public void setGoalName(String goalName) {
        this.goalName = goalName;
    }

    public Integer getGoalWeightageValue() {
        return goalWeightageValue;
    }

    public void setGoalWeightageValue(Integer goalWeightageValue) {
        this.goalWeightageValue = goalWeightageValue;
    }

    public String isGoalAchieved() {
        return goalAchieved;
    }

    public void setGoalAchieved(String goalAchieved) {
        this.goalAchieved = goalAchieved;
    }

    public String getGoalAchieved() {
        return goalAchieved;
    }
}
